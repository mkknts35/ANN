



//============================================================================
#include "sample.h"
//============================================================================
sample::sample(){}
//============================================================================
sample::~sample(){}
//============================================================================
void sample::addValue(double value){ 
    values.push_back(value); 
}
//============================================================================
void sample::setClassification(string classification){ 
    this->classification = classification; 
}
//============================================================================
vector<double> sample::getValues(){ 
    return values; 
}
//============================================================================
string sample::getClassification(){ 
    return classification; 
}
//============================================================================
void sample::printSample(){
    cout << classification;
    cout << " {";
    for (unsigned int i = 0; i < values.size(); i++) {
        cout << values.at(i);
        if (i < values.size() - 1) {
            cout << ", ";
        }
    }
    cout << "}" << endl;
}
